from sklearn.feature_extraction.text import TfidfVectorizer
import jieba

def cutword():
    c1 = jieba.cut("今天很残酷，明天更残酷，后天很美好,但绝对大部分是死在明天晚上，所以每个人不要放弃今天。")
    c2 = jieba.cut("我们看到的从很远星系来的光是在几百万年之前发出的，这样当我们看到宇宙时，我们是在看它的过去。")
    c3 = jieba.cut("如果只用一种方式了解某样事物，你就不会真正了解它。了解事物真正含义的秘密取决于如何将其与我们所了解的事物相联系。")

    # 转换为词组列表
    c1 = list(c1)
    c2 = list(c2)
    c3 = list(c3)
    #print(c1)

    # 转换为句子
    c1 = " ".join(c1)
    c2 = " ".join(c2)
    c3 = " ".join(c3)
    #print(c1)

    return c1,c2,c3

def tfvecvec():
    """
    对文本进行特征值化
    :return:
    """
    tfvec = TfidfVectorizer()

    c1, c2, c3 = cutword()
    

    result = tfvec.fit_transform([c1, c2, c3])

    print(tfvec.get_feature_names())
    print(result.toarray())

if __name__ == "__main__":
    tfvecvec()

